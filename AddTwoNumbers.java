public class AddTwoNumbers {
    public static void main(String[] args) {
        // Define two numbers directly
        int num1 = 10;
        int num2 = 20;

        // Calculate the sum of both numbers
        int sum = num1 + num2;

        // Display the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
